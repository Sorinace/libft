#include <string.h>
#include <stdlib.h>
#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	
	str = NULL;
	str = (char*)malloc(size);
	if (str != NULL)
		ft_bzero(str, size);
	return (str);
}